#Sum
print("----------Sum----------");
numbers = [10, 20, 30, 40, 50]
sum_of_numbers = sum(numbers)
print(sum_of_numbers)  # Output: 150


#Average
print("----------Average----------");
numbers = [10, 20, 30, 40, 50]
average = sum(numbers) / len(numbers)
print(average)  # Output: 30.0
print(int(average)) #Output: 30

